// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
//
//      NDK 回调函数
//
// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
void NetOpenHook()
{
    hUDP = DaemonNew(SOCK_DGRAM, 0, 1025, UDPServer, OS_TASKPRINORM, OS_TASKSTKNORM, 0, 1);
}

void NetCloseHook()
{
    DaemonFree(hUDP);
}

