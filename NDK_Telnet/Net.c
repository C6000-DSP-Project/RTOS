void NetOpenHook()
{
    hData = DaemonNew(SOCK_STREAM, 0, 1000, dtask_tcp_datasrv, OS_TASKPRINORM, OS_TASKSTKNORM, 0, 3);
    hNull = DaemonNew(SOCK_STREAMNC, 0, 1001, dtask_tcp_nullsrv, OS_TASKPRINORM, OS_TASKSTKNORM, 0, 3);
}

void NetCloseHook()
{
    DaemonFree(hData);
    DaemonFree(hNull);
}

