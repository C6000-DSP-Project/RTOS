//
//      获取 IP 地址
//
// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
void NetGetIPAddr(IPN IPAddr, unsigned int IfIdx, unsigned int fAdd)
{
    if(fAdd)
    {
        printf("Network Added: %d", IfIdx);
    }
    else
    {
        printf("Network Removed: %d", IfIdx);
    }

    char StrIP[16];
    NtIPN2Str(IPAddr, StrIP);
    printf("%s\r\n", StrIP);
}
