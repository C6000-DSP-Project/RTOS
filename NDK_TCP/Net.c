static HANDLE hTCPServer = 0;
static HANDLE hTCPClientTask;

// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
//
//      函数声明
//
// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
extern int TCPServer(SOCKET s, UINT32 unused);
extern void TCPClient();

// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
//
//      NDK 回调函数
//
// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
void NetOpenHook()
{
    // TCP 服务器 监听端口 1025
    hTCPServer = DaemonNew(SOCK_STREAMNC, 0, 1025, TCPServer, OS_TASKPRINORM, OS_TASKSTKNORM, 0, 3);

    // TCP 客户端任务
    // 也可以直接创建 SYS/BIOS 任务 但是任务优先级不能超过 NDK 协议栈核心任务优先级
    hTCPClientTask = TaskCreate(TCPClient, "TCP Client", OS_TASKPRINORM, OS_TASKSTKNORM, 0, 0, 0);
}

void NetCloseHook()
{
    DaemonFree(hTCPServer);
    TaskDestroy(hTCPClientTask);
}

